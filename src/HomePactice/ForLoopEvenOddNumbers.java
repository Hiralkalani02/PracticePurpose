package HomePactice;

public class ForLoopEvenOddNumbers {
    public static void main(String[] args) {

//2i  To print even numbers up to 20


        {
            for (int i = 0; i < 10; i++) {
                System.out.println(2 * i);
            }
        }
//2i+1 To print odd numbers up to 20
        {
            for (int j = 0; j < 10; j++)

            {
                System.out.println(2 * j + 1);
            }
        }
    }
}