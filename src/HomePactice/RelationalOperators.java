package HomePactice;

public class RelationalOperators {

    public static void main(String[] args) {
	// Relational Operators Practice
       int a=10;
       int b=20;
       System.out.println(a==b); //returns false because 10 is not equal to 20
        System.out.println(a!=b); //returns true because 10 is not equal to 20
        System.out.println(a>b); //False as 10 is not greater than 20
        System.out.println(a<b); // True as 10 is less than 20
        System.out.println(a>=b);//False as 10 is not greater or equal to 20
        System.out.println(a<=b); //True as 10 is less than 20 and one of the condition is true

    }
}
